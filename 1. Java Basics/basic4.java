// Data Types
// there are 2 types in data types
// 1. Primitive = byte = 1 byte,
// short = 2 byte
// char = 2 byte
// boolean = 1 byte
// int = 4 byte
// long = 8 byte
// float = 4 byte
// double = 8 byte
// 2. Non-Primitive = string, array, class, object, interface
public class basic4 {
    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        float price = (float) 10.5;
        System.out.println(price);
        int number = 25;
        System.out.println(number);
        // long
        // double
        short n = 240;
        System.out.println(n);
    }
}
