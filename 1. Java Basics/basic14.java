// Relational Operators

// Types

// == (Equal to operator)
// != (Not equal to operator)
// < (Greater then)
// > (Less then)
// >= (Less then equal to)
// <= (Greater then equal to)

public class basic14 {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        System.out.println(a == b);System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
